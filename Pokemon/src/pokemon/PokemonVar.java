package pokemon;

public class PokemonVar {

    String nome;
    String tipo;
    int vida;
    int lvl;
    int poder;
    String fraqueza;

    public PokemonVar(String nome, String tipo, int vida, int lvl, int poder, String fraqueza) {
        this.nome = nome;
        this.tipo = tipo;
        this.vida = vida;
        this.lvl = lvl;
        this.poder = poder;
        this.fraqueza = fraqueza;
    }
}
